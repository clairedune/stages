<H2>Soutenance</H2>
 
 <H3>Préparation de la soutenance </H3>

<p>Répétez l'oral plusieurs fois avec la montre en main. Travaillez la communication. Tout est porteur de sens.</p>

<p>Soignez le français et la langue étrangère. Allez directement à une vraie analyse. Ne perdez pas le temps avec le superficiel. Faite une brève synthèse. Insistez sur la problématique avec des solutions et du recul par rapport à votre expérience et de votre recherche. Pas de listes sans fin style "Google" mais plutôt votre expérience personnelle et raisonée toujours avec un lien à vos études et à votre avenir professionnelle.</p>


<H3>Langue étrangère</H3>
<p>Lors des soutenances tous les stagiaires doivent présenter une conclusion de 5 minutes en anglais.</p>

<p>Il est possible de présenter en espagnol ou italien si accord du jury 7 jours avant la soutenance. Si vous faites cette partie en anglais, pas besoin de se manifester, c’est automatique. Sinon, la demande doit être faite : </p>
<ul>
	<li> par email 
		<ul>
			<li>au tuteur SRC, </li>
			<li>au responsable des stages,</li>
			<li>à Madame Rabreau,</li>
		</ul></li>
	<li> par écrit sur le rapport.</li>
</ul>
 
<p>
Ne râtez pas la partie deuxième langue. Ce n'est rien par rapport à ce que l'on fait en cours. Répétez jusqu'à la perfection.
</p> 

 
